/**
 *op_add - addintion.
 *@a: Integer.
 *@b: Integer.
 *Return: The addition.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - substract.
 *@a: Integer.
 *@b: Integer.
 *Return: The substraction.
 */
int op_sub(int a, int b)

{
	return (a - b);
}
/**
 *op_mul - multiplication
 *@a: Integer.
 *@b: Integer.
 *Return: The result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - Division.
 *@a: Integer.
 *@b: Integer.
 *Return: The result of the division.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - The reminder of a division.
 *@a: Integer.
 *@b: Integer.
 *Return: The reminder of the division.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
